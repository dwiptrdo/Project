#  operasi aritmatika

a = 12
b = 3

# operasi tambah
hasil = a + b
print(a, "+", b, "=", hasil)

# operasi kurang
hasil = a - b
print(a, "-", b, "=", hasil)

# operasi kali
hasil = a * b
print(a, "*", b, "=", hasil)

# operasi bagi
hasil = a / b
print(a, "/", b, "=", hasil)

# operasi eksponen pangkat
hasil = a ** b
print(a, "**", b, "=", hasil)

# operasi eksponen modulus/sisa
hasil = a % b
print(a, "%", b, "=", hasil)

# operasi floar division //
hasil = a // b
print(a, "//", b, "=", hasil)