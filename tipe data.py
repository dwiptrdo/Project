# tipe data angka tanpa koma adalah int
data_integer = 11
print("data : ", data_integer)
print("bertipe :", type(data_integer))

# tipe data angka dengan koma adalah float
data_float = 2.5
print("data : ", data_float)
print("bertipe :", type(data_float))

# tipe data kumpulan karakter adalah string
data_string = "Alfredo"
print("data : ", data_string)
print("bertipe :", type(data_string))

# tipe data biner true/false adalah boolean
data_bool = True
print("data : ", data_bool)
print("bertipe :", type(data_bool))

## tipe data khusus
# tipe data complex
data_complex = complex(65)
print("data : ", data_complex)
print("bertipe :", type(data_complex))

# tipe data  dari bahasa C
from ctypes import c_double

data_c_double = c_double(6.5)
print("data : ", data_c_double)
print("bertipe :", type(data_c_double))


